# Запрашиваем целое число у пользователя
usr_num = input('Введите целое число: ')
# Одиночное число
single_num = int(usr_num)
# Двойное число
double_num = int(usr_num * 2)
# Тройное число
triple_num = int(usr_num * 3)
# Запрашиваемая сумма
total = single_num + double_num + triple_num
# Выводим полученную сумму в stdout
print(f'Сумма: {total}')


